        private void Add_IP_as_Host(int ciscoCommandId, string sIp)
        {
            // The order of this condition check is important!!!
            if (string.IsNullOrEmpty(sIp) || _cpObjects.HasObject(sIp) || !NetworkUtils.IsValidIp(sIp))
